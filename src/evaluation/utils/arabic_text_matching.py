"""Utilities for matching Arabic text, handling harakaat and transliterations."""

import re


def remove_harakaat(text: str) -> str:
    """
    Remove Arabic diacritical marks (harakaat/tashkeel) from text.

    Removes: َ ً ُ ٌ ِ ٍ ْ ّ (fatha, damma, kasra, sukun, shadda, tanween)

    Args:
        text: Arabic text with or without harakaat

    Returns:
        Text with harakaat removed

    Examples:
        >>> remove_harakaat("الكِتَاب")
        'الكتاب'
        >>> remove_harakaat("قَلَمٌ")
        'قلم'
    """
    # Unicode ranges for Arabic diacritics
    harakaat_pattern = r"[\u064B-\u0652\u0670]"
    return re.sub(harakaat_pattern, "", text)


def extract_arabic_words(text: str) -> list[str]:
    """
    Extract Arabic words from mixed text (may contain English, punctuation).

    Args:
        text: Text containing Arabic (may have English/numbers/punctuation)

    Returns:
        List of Arabic word strings

    Examples:
        >>> extract_arabic_words("Translate: القَلَم")
        ['القلم']
        >>> extract_arabic_words("كِتَاب (kitaab) - book")
        ['كتاب']
    """
    # Arabic character range: \u0600-\u06FF
    arabic_words = re.findall(r"[\u0600-\u06FF]+", text)
    # Remove harakaat from extracted words
    return [remove_harakaat(word) for word in arabic_words]


def extract_vocab_from_item(learned_item: str) -> str:
    """
    Extract just the Arabic vocabulary from a learned item string.

    Handles formats like:
    - "كِتَاب (kitaab) - book"
    - "القَلَم"
    - "مَدْرَسَة (madrasa) - school"

    Args:
        learned_item: Full learned item string with transliteration/translation

    Returns:
        Just the Arabic vocab, without harakaat

    Examples:
        >>> extract_vocab_from_item("كِتَاب (kitaab) - book")
        'كتاب'
        >>> extract_vocab_from_item("القَلَم (al-qalam) - the pen")
        'القلم'
        >>> extract_vocab_from_item("Gender agreement rule")
        ''
    """
    arabic_words = extract_arabic_words(learned_item)
    # Return the first (usually only) Arabic word found
    return arabic_words[0] if arabic_words else ""


def vocab_appears_in_text(vocab: str, text: str, substring_match: bool = True) -> bool:
    """
    Check if vocabulary appears in text, handling harakaat variations.

    Args:
        vocab: Arabic vocabulary word to search for (e.g., "كتاب")
        text: Text to search in (e.g., "الكتاب على الطاولة")
        substring_match: If True, match vocab as substring (finds "مدرسة" in "المدرسة")
                        If False, require exact word match

    Returns:
        True if vocab found in text, False otherwise

    Examples:
        >>> vocab_appears_in_text("قلم", "القَلَم")
        True
        >>> vocab_appears_in_text("كتاب", "الكِتَاب على الطَاوِلَة")
        True
        >>> vocab_appears_in_text("مدرسة", "في المدرسة")
        True
        >>> vocab_appears_in_text("قطة", "الكلب")
        False
    """
    # Normalize both by removing harakaat
    clean_vocab = remove_harakaat(vocab).strip()
    clean_text = remove_harakaat(text).strip()

    if not clean_vocab or not clean_text:
        return False

    if substring_match:
        return clean_vocab in clean_text
    else:
        # Word boundary match - check if vocab appears as complete word
        # Split on spaces and Arabic punctuation
        text_words = re.split(r"[\s\u060C\u061B\u061F]+", clean_text)
        return clean_vocab in text_words


def check_learned_items_usage(
    generated_text: str,
    learned_items: list[str],
    require_all: bool = False,
    min_usage_count: int = 1,
) -> tuple[bool, list[str], list[str]]:
    """
    Check if generated text uses learned vocabulary items.

    Args:
        generated_text: The text generated by the model
        learned_items: List of learned vocabulary items (may include transliterations)
        require_all: If True, ALL learned items must be used. If False, at least min_usage_count
        min_usage_count: Minimum number of items that must be used (if require_all=False)

    Returns:
        Tuple of (passed, used_items, unused_items)
        - passed: True if usage requirements met
        - used_items: List of learned items that were found
        - unused_items: List of learned items that were NOT found

    Examples:
        >>> text = "الكِتَاب على الطَّاوِلَة"
        >>> items = ["كِتَاب (kitaab) - book", "طَاوِلَة (taawila) - table"]
        >>> passed, used, unused = check_learned_items_usage(text, items)
        >>> passed
        True
        >>> len(used)
        2
    """
    used_items = []
    unused_items = []

    for item in learned_items:
        vocab = extract_vocab_from_item(item)

        # Skip non-Arabic items (like "Gender agreement rule")
        if not vocab:
            continue

        if vocab_appears_in_text(vocab, generated_text):
            used_items.append(item)
        else:
            unused_items.append(item)

    # Determine if passed
    if require_all:
        passed = len(unused_items) == 0 and len(used_items) > 0
    else:
        passed = len(used_items) >= min_usage_count

    return passed, used_items, unused_items


def format_usage_result(passed: bool, used_items: list[str], unused_items: list[str]) -> str:
    """
    Format the vocab usage check result as a human-readable string.

    Args:
        passed: Whether the check passed
        used_items: List of items that were used
        unused_items: List of items that were not used

    Returns:
        Formatted string like "✓ Learned items: 2/3 used (book, table)"

    Examples:
        >>> format_usage_result(True, ["كتاب (book)"], [])
        '✓ Learned items: 1 used (كتاب)'
        >>> format_usage_result(False, [], ["قلم (pen)", "مدرسة (school)"])
        '✗ Learned items: 0 used (expected 2)'
    """
    symbol = "✓" if passed else "✗"
    total = len(used_items) + len(unused_items)
    used_count = len(used_items)

    if used_count == 0:
        return f"{symbol} Learned items: none used (expected {total})"
    elif used_count == total:
        # Extract just Arabic words for display
        vocab_display = ", ".join(
            [extract_vocab_from_item(item) or item[:20] for item in used_items[:3]]
        )
        return f"{symbol} Learned items: all {total} used ({vocab_display})"
    else:
        vocab_display = ", ".join(
            [extract_vocab_from_item(item) or item[:20] for item in used_items[:3]]
        )
        return f"{symbol} Learned items: {used_count}/{total} used ({vocab_display})"
